#include "Channel.h"

// ------------------------------------------------------------------------------------------------
// Public

// ================================================================================================
// Constructor
// ================================================================================================
Channel::Channel(const bool muted):

  // Initialize members
  _muted(muted)

{}

// ================================================================================================
// Mute the audio channel
// ================================================================================================
void Channel::mute() {

  // Mute the audio channel
  _muted = true;

}

// ================================================================================================
// Set a mute state
// ================================================================================================
void Channel::setMuteState(const bool state) {

  _muted = state;

}

// ================================================================================================
// Unmute the audio channel
// ================================================================================================

// ================================================================================================
// Check if audio channel is muted
// ================================================================================================
bool Channel::getMuteState() {

  // Check and return if audio channel is muted
  return _muted;

}