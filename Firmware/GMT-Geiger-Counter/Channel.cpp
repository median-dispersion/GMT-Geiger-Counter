#include "Channel.h"

// ------------------------------------------------------------------------------------------------
// Public

// ================================================================================================
// Constructor
// ================================================================================================
Channel::Channel(const bool muted):

  // Initialize members
  _muted(muted)

{}

// ================================================================================================
// Mute the audio channel
// ================================================================================================
void Channel::mute() {

  // Mute the audio channel
  _muted = true;

}

// ================================================================================================
// Unmute the audio channel
// ================================================================================================
void Channel::unmute() {

  _muted = false;

}

// ================================================================================================
// Check if audio channel is muted
// ================================================================================================
bool Channel::muted() {

  // Check and return if audio channel is muted
  return _muted;

}